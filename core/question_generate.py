class LCM(QuestionTemplate):
    def lcm(self, a: int, b: int) -> int:
        return (a * b) // gcd(a, b)

    def generate(self) -> dict:
        a = random.randint(10, 50)
        b = random.randint(10, 50)

        return {
            "question": f"{a} LCM {b}",
            "answer": self.lcm(a, b),
        }

