import random
from abc import ABC, abstractmethod
import time

class QuestionTemplate(ABC):
    @abstractmethod
    def generate(self) -> dict:
        """
        Must return:
        {
            "question": str,
            "answer": int
        }
        """
        pass

def gcd(x: int, y: int) -> int:
    while y:
        x, y = y, x % y
    return x

class PrimeSum(QuestionTemplate):
    def sum_prime_factors(self, n: int) -> int:
        total = 0
        i = 2

        while i * i <= n:
            if n % i == 0:
                while n % i == 0:
                    n //= i
                    total += i
            i += 1

        if n > 1:
            total += n

        return total

    def generate(self) -> dict:
        a = random.randint(10, 30)
        b = random.randint(10, 30)
        n = a * b

        return {
            "question": f"Find the sum of prime factors of {n}",
            "answer": self.sum_prime_factors(n),
        }

