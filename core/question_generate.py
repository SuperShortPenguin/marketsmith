QUESTION_REGISTRY = [
    PrimeSum(),
    MOD(),
    LCM(),
    GCD(),
    xor(),
    sum_cubes(),
    product(),
    AND(),
    OR(),
    Sum()
]

def generate_question() -> dict:
    """
    Public API for question generation.

    Returns:
        {
            "question": str,
            "answer": int
        }
    """
    template = random.choice(QUESTION_REGISTRY)
    ques = template.generate()

    ans = ques["answer"]
    ans_2 = 0
    
    while ans > 0 :
        ans_2 += ans % 10
        ans //= 10

        if ans == 0 and ans_2 > 9:
            ans = ans_2
            ans_2 = 0
    
    return {
        "question": ques["question"],
        "answer": ans_2
    }

